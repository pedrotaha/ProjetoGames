    public void buscar() {
        jfcImagem.setFileSelectionMode(jfcImagem.FILES_ONLY);
        int i = jfcImagem.showSaveDialog(null);
        if (i == 1) {
            jTextField1.setText("");
        } else {
            File arquivo = jfcImagem.getSelectedFile();
            jTextField1.setText(arquivo.getPath());
        }
    }
